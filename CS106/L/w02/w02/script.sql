CREATE TABLE Khoa
(
	MaKhoa VARCHAR(5),
	TenKhoa NVARCHAR(30),
	NamTL INT,
	Phong CHAR(3),
	DienThoai VARCHAR(11),
	TruongKhoa CHAR(3),
	NgayNhanChuc DATE,

	CONSTRAINT PK_Khoa PRIMARY KEY(MaKhoa)
)

CREATE TABLE BoMon
(
	MaBM VARCHAR(5),
	TenBoMon NVARCHAR(30),
	Phong CHAR(3),
	DienThoai VARCHAR(11),
	TruongBoMon CHAR(3),
	MaKhoa VARCHAR(5),
	NgayNhanChuc DATE,

	CONSTRAINT PK_BoMon PRIMARY KEY(MaBM)
)


-- Create foreign keys

ALTER TABLE GiaoVien
