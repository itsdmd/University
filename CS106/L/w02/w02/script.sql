(
	MaGV CHAR(3),
	MaDT CHAR(3),
	STT INT,
	PhuCap FLOAT,
	KetQua NVARCHAR(4),

	CONSTRAINT PK_ThamGiaDT PRIMARY KEY(MaGV, MaDT, STT),
)


-- Create foreign keys

ALTER TABLE DeTai
