-- Create tables

CREATE TABLE GiaoVien
(
	MaGV CHAR(3),
	TenGV NVARCHAR(30),
	Luong INT,
	Phai NVARCHAR(3),
	NgSinh DATE,
	DiaChi NVARCHAR(50),
