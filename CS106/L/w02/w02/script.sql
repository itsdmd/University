(
	MaDT CHAR(3),
	SoTT INT,
	TenCV NVARCHAR(50),
	NgayBD DATE,
	NgayKT DATE,

	CONSTRAINT PK_CongViec PRIMARY KEY(MaDT, SoTT),
)

