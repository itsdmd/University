	FOREIGN KEY(GVQLCM)
	REFERENCES GiaoVien(MaGV)
	
ALTER TABLE GiaoVien
	ADD CONSTRAINT FK_GiaoVien__MaBM
	FOREIGN KEY(MaBM)
	REFERENCES BoMon(MaBM)

ALTER TABLE Khoa
	ADD CONSTRAINT FK_Khoa__MaGV
	FOREIGN KEY(TruongKhoa)
	REFERENCES GiaoVien(MaGV)

ALTER TABLE BoMon
	ADD CONSTRAINT FK_BoMon__MaGV
	FOREIGN KEY(TruongBoMon)
	REFERENCES GiaoVien(MaGV)

ALTER TABLE BoMon
	ADD CONSTRAINT FK_BoMon__MaKhoa
