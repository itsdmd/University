#include <iostream>
using namespace std;

int main()
{
    int inp, outp = 1;

    cout << "Nhap so nguyen duong: ";
    cin >> inp;

    if (inp <= 0)
    {
        cout << "So nhap vao phai la so nguyen duong.";
        main();
    }

    else
    {
        for (int i = 1; i <= inp; i++)
        {
            outp *= i;
        }

        cout << outp;
    }
    
    return 0;
}