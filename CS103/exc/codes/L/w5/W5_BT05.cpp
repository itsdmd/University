#include <iostream>
using namespace std;



int uscln(long long, long long);



/// -------------------------------------------------------------------------- ///
///                                   main()                                   ///
/// -------------------------------------------------------------------------- ///
int main()
{
    long long a, b;

    cout << endl << "Nhap hai so nguyen a, b: ";
    cin >> a >> b;

    cout << endl << "USCLN cua " << a << " va " << b << " la: " << uscln(a, b) << endl;
    
    return 0;
}



/// -------------------------------------------------------------------------- ///
///                                    uscln()                                   ///
/// -------------------------------------------------------------------------- ///
int uscln(long long a, long long b)
{
    if (b == 0)
        return a;
    
    return uscln(b, a % b);
}